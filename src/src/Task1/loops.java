package Task1;


//	write a program to print the numbers from 10 to 50 using for loop/while loop
//	basic using loop java

public class loops{
	    public static void main(String[] args) {
	       
	        System.out.println("Printing numbers from 10 to 50 using a for loop:");
	        for (int i = 10; i <= 50; i++) {
	            System.out.println(i);
	        }

	       
	        System.out.println("Printing numbers from 10 to 50 using a while loop:");
	        int j = 10;  
	        while (j <= 50) {
	            System.out.println(j);
	            j++;  
	        }
	    }
}
	


